    <h4>Perpustakaan</h4>
        Perpustakaan SMKN 2 Bangkalan merupakan fasilitas pendidikan yang menyediakan akses dan layanan informasi untuk mendukung proses pembelajaran, penelitian, dan pengembangan akademik siswa dan guru. Sebagai unit kerja pelaksana teknis (UPT) SMKN 2 Bangkalan, perpustakaan bertanggung jawab dalam memilah, menghimpun, mengolah, serta menyebarluaskan sumber informasi yang relevan kepada komunitas sekolah.

    <p>
    <h4>Perpustakaan SMKN 2 Bangkalan</h4>
        Perpustakaan SMKN 2 Bangkalan berperan aktif dalam mendukung kegiatan akademik dan pengembangan diri siswa serta tenaga pendidik. Dengan lokasi yang mudah diakses dan koleksi yang beragam, perpustakaan memberikan layanan yang sesuai dengan kebutuhan belajar dan penelitian. Koleksi perpustakaan meliputi buku, materi referensi, jurnal, majalah, dan sumber informasi lainnya yang relevan dengan kurikulum sekolah.

    <p>
        Dalam lima tahun terakhir, perpustakaan SMKN 2 Bangkalan telah mengalami peningkatan yang signifikan dalam koleksi bahan bacaan serta layanan yang disediakan. Dukungan dan perhatian dari pihak sekolah memperkuat upaya pengembangan dan peningkatan kualitas layanan perpustakaan.

    <p>
